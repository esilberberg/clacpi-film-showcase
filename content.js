function translateContent(language) {
    // To translate mainContent object
    for (const contentKey in mainContent) {
        const element = document.getElementById(contentKey);

        if (element) {
            const translation = mainContent[contentKey].find(item => item[language]);
            if (translation) {
                element.textContent = translation[language];
            }
        }
    }
     // To translate Organizers object
    const organizersDisplay = document.getElementById('organizers-display');
    if (organizersDisplay) {
        let organizersHTML = '';

        organizersContent.forEach(organizer => {
            let biographyHTML = '';
            organizer.biography.forEach(bio => {
                const translation = bio.paragraph.find(item => item[language]);
                if (translation) {
                    biographyHTML += `<p>${translation[language]}</p>`;
                }
            });

            organizersHTML += `
                <div class="organizer">
                    <img src="2025media/people/${organizer.image}" alt="${organizer.name}">
                    <div>
                        <h3>${organizer.name}</h3>
                        ${biographyHTML}
                    </div>  
                </div>
            `;
        });
        organizersDisplay.innerHTML = organizersHTML; 
    }
};

document.getElementById('englishBtn').addEventListener('click', function() {
    translateContent('en');
    hamburgerMenu.classList.remove('open');
    navbarLinks.classList.remove('show');
});

document.getElementById('spanishBtn').addEventListener('click', function() {
    translateContent('es');
    hamburgerMenu.classList.remove('open');
    navbarLinks.classList.remove('show');
});

