function translateContent(language) {
    // To translate mainContent object
    for (const contentKey in mainContent) {
        const element = document.getElementById(contentKey);

        if (element) {
            const translation = mainContent[contentKey].find(item => item[language]);
            if (translation) {
                element.textContent = translation[language];
            }
        }
    }

    // To translate Organizers object
    const organizersDisplay = document.getElementById('organizers-display');
    if (organizersDisplay) {
        let organizersHTML = '';

        organizersContent.forEach(organizer => {
            let biographyHTML = '';
            organizer.biography.forEach(bio => {
                const translation = bio.paragraph.find(item => item[language]);
                if (translation) {
                    biographyHTML += `<p>${translation[language]}</p>`;
                }
            });

            organizersHTML += `
                <div class="organizer">
                    <img src="2025media/people/${organizer.image}" alt="${organizer.name}">
                    <div>
                        <h3>${organizer.name}</h3>
                        ${biographyHTML}
                    </div>
                </div>
            `;
        });
        organizersDisplay.innerHTML = organizersHTML;
    }

    // Translate and display films by date
    const translateAndDisplayFilms = (date, data) => {
        const filmDisplayDiv = document.getElementById(`film-display-${date}`);
        if (filmDisplayDiv) {
            filmDisplayDiv.innerHTML = '';

            data.forEach(sectionData => {
                const sectionTitleTranslation = sectionData.section.find(item => item[language]);
                const sectionTitleText = sectionTitleTranslation ? sectionTitleTranslation[language] : (sectionData.section.find(item => item.en) ? sectionData.section.find(item => item.en).en : sectionData.section[0].en); // Fallback to English

                const sectionTitle = document.createElement('h4');
                sectionTitle.textContent = sectionTitleText;
                filmDisplayDiv.appendChild(sectionTitle);

                let filmsListHTML = '<div class="film-list">';
                sectionData.films.forEach(film => {
                    const imageHTML = film.image ? `<img class="film-image" src="2025media/films/${film.image}">` : '';

                    const titleTranslation = film.titleTranslations ? film.titleTranslations.find(item => item[language]) : null;
                    const filmTitle = titleTranslation ? titleTranslation[language] : film.title;

                    let directorsHTML = film.directors.map(director => {
                        const directorTranslation = film.directorTranslations ? film.directorTranslations.find(item => item[language] && item.name === director) : null;
                        return directorTranslation ? directorTranslation[language] : director;
                    }).join('<br>');

                    let synopsisHTML = '';
                    film.synopsis.forEach(synopsis => {
                        const translation = synopsis[language];
                        if (translation) {
                            synopsisHTML = `<p class="film-synopsis">${translation}</p>`;
                            return; 
                        }
                    });
                    if (!synopsisHTML && film.synopsis.length > 0) {
                        const englishSynopsis = film.synopsis.find(item => item.en);
                        if (englishSynopsis) {
                            synopsisHTML = `<p class="film-synopsis">${englishSynopsis.en}</p>`; // Fallback to English synopsis
                        }
                    }

                    filmsListHTML += `
                        <div class="film">
                            <div class="film-image-container">${imageHTML}</div>
                            <p class="film-title">${filmTitle}</p>
                            <p class="film-directors">${directorsHTML}</p>
                            ${synopsisHTML}
                            <p class="film-languages">${film.languages}</p>
                        </div>
                    `;
                });
                filmsListHTML += '</div>';
                filmDisplayDiv.innerHTML += filmsListHTML;
            });
        }
    };

    // Call the DRY function for each date
    translateAndDisplayFilms('april-23', filmsContentApril23);
    translateAndDisplayFilms('april-24', filmsContentApril24);
    translateAndDisplayFilms('april-25', filmsContentApril25);
}



